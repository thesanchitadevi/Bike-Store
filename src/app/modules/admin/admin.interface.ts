import { Model, Types } from 'mongoose';

export type TGender = 'male' | 'female' | 'other';
export type TBloodGroup =
  | 'A+'
  | 'A-'
  | 'B+'
  | 'B-'
  | 'AB+'
  | 'AB-'
  | 'O+'
  | 'O-';

export type TUserName = {
  firstName: string;
  middleName: string;
  lastName: string;
};

export interface IAdmin {
  id: string;
  user: Types.ObjectId;
  designation: string;
  name: TUserName;
  gender: TGender;
  dateOfBirth?: Date;
  email: string;
  contactNumber: string;
  bloodGroup?: TBloodGroup;
  avatar?: string;
  isDeleted: boolean;
}

export interface AdminStaticMethods extends Model<IAdmin> {
  isAdminExists(id: string): Promise<IAdmin | null>;
}
